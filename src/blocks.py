import torch
import torch.nn as nn
from e3nn import o3
from torch_geometric.utils import scatter

class EquivariantMessagePassing(nn.Module):
    """
    Computes equivariant messages from neighbor atoms to the central atom.
    Message = R(r_ij) * (Node_j \otimes Y(r_hat_ij))
    """
    def __init__(self, node_irreps: o3.Irreps, edge_irreps: o3.Irreps, radial_dim: int, hidden_irreps: o3.Irreps):
        super().__init__()
        self.node_irreps = o3.Irreps(node_irreps)
        self.edge_irreps = o3.Irreps(edge_irreps)
        self.hidden_irreps = o3.Irreps(hidden_irreps)
        
        # Tensor product to combine node features (j) with edge features (Spherical Harmonics)
        self.tp = o3.FullyConnectedTensorProduct(
            self.node_irreps,
            self.edge_irreps,
            self.hidden_irreps,
            shared_weights=False # Weights are dynamically generated by the radial network
        )
        
        # Radial network: MLP that maps radial distances to the weights of the Tensor Product
        self.radial_net = nn.Sequential(
            nn.Linear(radial_dim, 64),
            nn.SiLU(),
            nn.Linear(64, self.tp.weight_numel)
        )

    def forward(self, node_feat: torch.Tensor, edge_index: torch.Tensor, radial_feat: torch.Tensor, sh_feat: torch.Tensor) -> torch.Tensor:
        """
        Args:
            node_feat: [num_nodes, dim_node]
            edge_index: [2, num_edges]
            radial_feat: [num_edges, radial_dim]
            sh_feat: [num_edges, dim_sh]
        Returns:
            Aggregated messages at nodes: [num_nodes, dim_hidden]
        """
        row, col = edge_index
        
        # 1. Generate weights from radial distances
        tp_weights = self.radial_net(radial_feat) # [num_edges, weight_numel]
        
        # 2. Gather node features of neighbors (col)
        node_j = node_feat[col] # [num_edges, dim_node]
        
        # 3. Compute message via Tensor Product
        # We multiply the neighbor's node feature with the spherical harmonic of the edge
        # The connection weight is parameterized by the distance (radial_feat)
        message = self.tp(node_j, sh_feat, tp_weights) # [num_edges, dim_hidden]
        
        # 4. Aggregate messages to target node (row)
        num_nodes = node_feat.shape[0]
        # scatter() sums the messages pointing to the same target node
        out = scatter(message, row, dim=0, dim_size=num_nodes, reduce='sum')
        
        return out

class SimpleMACEBlock(nn.Module):
    """
    A simplified MACE block that performs:
    1. Message passing to get 2-body features (A_i)
    2. A single tensor product of A_i with itself to generate 3-body features (B_i).
    3. Residual connection and update.
    """
    def __init__(self, node_irreps: str, sh_irreps: str, radial_dim: int):
        super().__init__()
        self.node_irreps = o3.Irreps(node_irreps)
        self.sh_irreps = o3.Irreps(sh_irreps)
        
        # Use the same irreps for hidden to keep it simple
        self.hidden_irreps = self.node_irreps
        
        # Message passing (2-body)
        self.mp = EquivariantMessagePassing(
            self.node_irreps, 
            self.sh_irreps, 
            radial_dim, 
            self.hidden_irreps
        )
        
        # Symmetric Contraction approximation (3-body)
        # We take the aggregated message (A_i) and tensor square it
        # TensorSquare efficiently computes only the symmetric paths for x \otimes x
        self.tensor_square = o3.TensorSquare(self.hidden_irreps)
        self.contraction = o3.Linear(self.tensor_square.irreps_out, self.node_irreps)
        
        # Linear layer for updating node features
        self.update = o3.Linear(self.node_irreps, self.node_irreps)

    def forward(self, node_feat: torch.Tensor, edge_index: torch.Tensor, radial_feat: torch.Tensor, sh_feat: torch.Tensor) -> torch.Tensor:
        # 1. Message Passing -> A_i (2-body equivariant correlations)
        A_i = self.mp(node_feat, edge_index, radial_feat, sh_feat)
        
        # 2. "Contraction" -> B_i (3-body equivariant correlations)
        # By squaring the aggregated 2-body messages, we effectively mix information from two different neighbors
        B_i = self.contraction(self.tensor_square(A_i))
        
        # 3. Update & Residual
        out = self.update(B_i) + node_feat
        return out
